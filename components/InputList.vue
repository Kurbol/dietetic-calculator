<template>
  <tool-card title="Inputs">
    <template #toolbar>
      <v-btn
        flat
        icon
        color="secondary"
        @click="setAllCalculatorsSelected(true)"
      >
        <v-icon>check_box</v-icon>
      </v-btn>
      <v-btn
        flat
        icon
        color="secondary"
        @click="setAllCalculatorsSelected(false)"
      >
        <v-icon>check_box_outline_blank</v-icon>
      </v-btn>
    </template>
    <v-container>
      <v-layout column align-center>
        <v-slide-y-transition group>
          <v-flex v-for="input in activeInputs" :key="input.id">
            <v-layout>
              <v-text-field
                :label="input.name"
                :value="input.value"
                @input="setInputValue({ id: input.id, value: $event })"
              ></v-text-field>
              <v-select
                outlined
                class="units"
                item-text="symbol"
                item-value="symbol"
                label="Units"
                :items="getUnitsByType(input.type)"
                :value="input.selectedUnit"
                @input="
                  setInputSelectedUnit({ id: input.id, selectedUnit: $event })
                "
              >
                <template slot="item" slot-scope="data">
                  <span>{{ data.item.name }} ({{ data.item.symbol }})</span>
                </template>
              </v-select>
            </v-layout>
          </v-flex>
        </v-slide-y-transition>
      </v-layout>
    </v-container>
  </tool-card>
</template>

<script>
import { mapState, mapGetters, mapActions } from 'vuex'
import ToolCard from '@/components/ToolCard.vue'

export default {
  components: {
    ToolCard
  },
  data() {
    return {
      items: ['kg', 'lb']
    }
  },
  computed: {
    ...mapState('calcs', ['inputs']),
    ...mapGetters('calcs', ['activeInputs', 'getUnitsByType'])
  },
  methods: {
    ...mapActions('calcs', ['setInputValue', 'setInputSelectedUnit'])
  }
}
</script>

<style lang="scss" scoped>
.units {
  width: 60px;
}
</style>
